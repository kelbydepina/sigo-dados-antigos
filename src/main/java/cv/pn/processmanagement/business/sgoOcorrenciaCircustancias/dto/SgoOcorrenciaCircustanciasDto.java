package cv.pn.processmanagement.business.sgoOcorrenciaCircustancias.dto;


public class SgoOcorrenciaCircustanciasDto {

    private String circustancia;

    private String resposta;

    private Long idOcorrencia;

    public String getCircustancia() {
        return circustancia;
    }

    public void setCircustancia(String circustancia) {
        this.circustancia = circustancia;
    }

    public String getResposta() {
        return resposta;
    }

    public void setResposta(String resposta) {
        this.resposta = resposta;
    }

    public Long getIdOcorrencia() {
        return idOcorrencia;
    }

    public void setIdOcorrencia(Long idOcorrencia) {
        this.idOcorrencia = idOcorrencia;
    }
}
